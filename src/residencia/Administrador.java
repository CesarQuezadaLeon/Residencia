    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String bd = "ProyectoResidencia";
        String login = "CrLeon";
        String password = "jake12345";
        String url = "jdbc:sqlserver://localhost:1433;databaseName=" + bd + ";integratedSecutiry=false";
        Connection connection = null;
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver").newInstance();
            connection = DriverManager.getConnection(url, login, password);
            if (connection != null) {
                
                PreparedStatement pst = connection.prepareStatement("insert into Usuarios values(?,?,?)");
                pst.setString(1, txtUsuario.getText().trim());
                pst.setString(2, txtPassword.getText().trim());
                pst.setInt(3, CMBNivel.getSelectedIndex() + 1);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Registro Realizado");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error" + ex.toString());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error" + ex.toString());
        }
        
        CMBNivel.setSelectedIndex(0);
        txtUsuario.setText("");
        txtPassword.setText("");

    }//GEN-LAST:event_jButton2ActionPerformed

